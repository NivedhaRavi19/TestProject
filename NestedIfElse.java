package org.org;

public class NestedIfElse {
	public static void main(String[] args) {
		   int i=18;
		   if (i>10) //18>10
		   {
		   if (i*2==0)//18*2==36
			   System.out.println("i is an even number and greater than 10");
		   
		   else 
			   System.out.println("i is odd number and greater than 10");
		  }
		   else 
		   { 
			   System.out.println("i is smaller than 10");
		   }
		  }}
		   
