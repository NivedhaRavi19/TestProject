package org.org;

public class BeforeAfterSwapping {
public static void main(String[] args) {
		
		int a=10,b=20;
		//Before swapping
		a=a+b;//10+20
		a=a-b;//30-20
	    a=a-b;//30-10
		
		System.out.println("a=  "+a );
		System.out.println("b= "+b);
	

      //After swapping
	System.out.println("a="+a);
	System.out.println("b="+b);
			
	        
}}

